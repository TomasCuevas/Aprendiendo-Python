"""
    # MODULO MATH:
        El módulo math contiene un conjunto de métodos y
        constantes que se pueden utilizar para resolver tareas
        matemáticas de mayor complejidad. Es el equivalente a la
        calculadora científica dentro de Python.

        # Algunas de las situaciones que pueden ayudarnos a resolver son:
            - Relaciones trigonométricas (seno, coseno, tangente, sus inversas e hiperbólicas)
            - Funciones logarítmicas
            - Potencias y raíces
            - Combinatoria y permutaciones
            - Redondeos
            - Factoriales
"""


import math

resultado = math.floor(12.73)
print(f"Método FLOOR: {resultado}")

resultado = math.ceil(12.01)
print(f"Método CEIL: {resultado}")

resultado = math.sqrt(144)
print(f"Método SQRT: {resultado}")

resultado = math.factorial(5)
print(f"Método FACTORIAL: {resultado}")

resultado = math.pi
print(f"Método PI: {resultado}")

resultado = math.log(100, 5)
print(f"Método LOG: {resultado}")

resultado = math.exp(1)
print(f"Método EXP: {resultado}")

resultado = math.tan(2645)
print(f"Método TAN: {resultado}")

resultado = math.sin(17)
print(f"Método SIN: {resultado}")

