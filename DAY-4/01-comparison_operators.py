# OPERADORES DE COMPARACION: Como su nombre lo indica, sirven para comparar dos o másvalores. El resultado de esta comparación es un booleano(True/False)

# IGUAL A ==
# DIFERENTE A !=
# MAYOR QUE >
# MENOR QUE <
# MAYOR O IGUAL A >=
# MENOR O IGUAL A <=

